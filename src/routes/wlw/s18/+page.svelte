        <h4>Een korte geschiedenis van Spraakherkenning</h4>
        <p>Spraakherkenning heeft een lange weg afgelegd sinds de eerste experimenten in de jaren 1950. Hier is een overzicht van de belangrijkste mijlpalen:</p>
        <ul>
            <li>1950s-1960s: Eerste pogingen om spraakherkenning te ontwikkelen. Deze systemen konden slechts enkele woorden herkennen.</li>
            <li>1970s-1980s: Verbeteringen brachten meer nauwkeurigheid, maar de technologie bleef beperkt tot eenvoudige toepassingen.</li>
            <li>1990s: Software zoals DragonDictate en IBM ViaVoice maakte spraakherkenning toegankelijker voor consumenten.</li>
            <li>2000s: Google introduceerde spraakherkenning en integreerde dit in zijn zoekdiensten.</li>
            <li>2010s: De opkomst van slimme assistenten zoals Google Assistant, Alexa en Siri bracht spraakherkenning naar de mainstream.</li>
            <li>2020s: Technologieën zoals live transcriptie werden alledaags en steeds nauwkeuriger.</li>
        </ul>
    </article>

    <article>
        <h4>Web Speech API</h4>
        <p><strong>Hoe werkt het?</strong></p>
        <ul>
            <li>De gebruiker spreekt.</li>
            <li>De API registreert en verwerkt de audio.</li>
            <li>De woorden worden vertaald naar tekst of acties.</li>
        </ul>
        <p>Deze technologie maakt spraakgestuurde interacties mogelijk, zoals live transcripties, spraakgestuurde zoekopdrachten en assistenten die commando's begrijpen.</p>
    </article>

